                    <p>
                        <h4><strong>What is the Turing Test in Artificial Intelligence?</strong></h4>
                           <br>The basis of the Turing Test is that the Artificial Intelligence entity should be able to hold a conversation with a human agent. The human agent ideally should not able to conclude that they are talking to an Artificial Intelligence. To achieve these ends, the AI needs to possess these qualities:

                           <br>Natural Language Processing to communicate successfully.
                           <br>Knowledge Representation to act as its memory.
                           <br>Automated Reasoning to use the stored information to answer questions and draw new conclusions.
                           <br> Machine Learning to detect patterns and adapt to new circumstances.
                           <h4><strong>Cognitive Modelling Approach</strong></h4>
                           <br>As the name suggests, this approach tries to build an Artificial Intelligence model based on Human Cognition. To distil the essence of the human mind, there are 3 approaches:

                           <br>1.Introspection: observing our thoughts, and building a model based on that
                           <br>2. Psychological Experiments: conducting experiments on humans and  observing their behaviour
                           <br>3.Brain Imaging: Using MRI to observe how the brain functions in different scenarios and replicating that through code.
                           <br><h3><strong>The Laws of Thought Approach</strong></h3>
                           <br> The Laws of Thought are a large list of logical statements that govern the operation of our mind. The same laws can be codified and applied to artificial intelligence algorithms. The issues with this approach, because solving a problem in principle (strictly according to the laws of thought) and solving them in practice can be quite different, requiring contextual nuances to apply. Also, there are some actions that we take without being 100% certain of an outcome that an algorithm might not be able to replicate if there are too many parameters.

                           <br>The Rational Agent Approach 
                           <br> A rational agent acts to achieve the best possible outcome in its present circumstances.
                           <br>According to the Laws of Thought approach, an entity must behave according to the logical statements. But there are some instances, where there is no logical right thing to do, with multiple outcomes involving different outcomes and corresponding compromises. The rational agent approach tries to make the best possible choice in the current circumstances. It means that it’s a much more dynamic and adaptable agent.
                           <br>Now that we understand how Artificial Intelligence can be designed to act like a human, let’s take a look at how these systems are built.
