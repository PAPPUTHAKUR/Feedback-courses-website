               <h4><strong>Advantages of DBMS</strong></h4>
               <br>DBMS can control database redundancy
               <br>DBMS allow authorized users of the organizations to share the data.
               <br>DBMS can be maintained easily due to its centralized nature.
               <br>DBMS reduces development time and maintenance need.
               <br>DBMS provides backup and recovery subsystems
               <br>DBMS offers Data Integrity and Security
              <h4><strong>Disadvantages of DBMS</strong></h4>
              <br>DBMS requires a high-speed data processor so the cost of both hardware and software is high.
              <br>DBMS occupies a large space of disks and memory.
              <br>In DBMS all the data is stored in a single database, and if the databases are damaged due to power failure then we may lose all the data.
              <br>DBMS system is a complex system, it requires training for the users to use DBMS.
              <br>DBMS can’t perform high-end calculations
              <h4><strong>Application of DBMS</strong></h4>
              <br>Some of the fields where a database management system is used given below:

              <br>HR Management:  Employees information and salary-related activities can be managed in DBMS software.

              <br>Banking: Customer information, account activities, payments, deposits, loans can be managed by DBMS software
              <br>Educational Institution: All the information regarding the students is handled by DBMS software.
              <br>Finance: For storing information about stock, sales, is handled by DBMS software.
              <br>Sales: DBMS is used for storing customer’s information.
