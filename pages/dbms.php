                  <h4><strong>What is DBMS?</strong></h4>
                  <br>Database Management System (DBMS) is software that enables its users to manage, access the databases and also helps in manipulating and reporting the data. Some of the examples of DBMS are MySQL, PostgreSQL, Microsoft Access, SQL Server, Oracle, etc. 
                  <br>This DMBS tutorial explains the basic of DMBS such as important terminologies, types of DBMS,E-R model, relation model,Constraints and much more.
                 
                  <br>DBMS has a wide range of applications in various sectors like banking, Finance, Telecom, Manufacturing etc. Traditional file systems have certain drawbacks that lead to the databases. Those are listed below: 
                  <br>1.Main memory will be limited and the data to be processed needs more space.
                  <br>2.Special application programs are needed for the specific needs of the users.
                  <br>3.Consistency issues arise during system crashes.

