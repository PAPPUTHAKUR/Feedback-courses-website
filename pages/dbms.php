              <h4>What is Database?</h4>
                <br> database is defined as a collection of data, that helps in creating, editing and maintaining database files and records. Database software handles data storage, data backup, and security. Database software is also called as “database management system” (DBMS).

                <br>Examples of database are: Microsoft Access, Oracle, MySQL, FoxPro etc. 

                <br>DBMS makes the preceding tasks easier. The main characteristics of the database approach are- 

                <br>1.Self-describing nature of the database.
                <br> 2.Programs and data are insulated such that change in the data does not require a change in the program.
                <br> 3. Same data can be shown differently to different viewers.
                <br> 4. Data can be shared between the users, and transactions can be interleaved maintaining consistency.
                <p>
                    <h4><strong>Why Learn DBMS?</strong></h4>
                    <br>This DBMS tutorial helps you to understand why to learn DBMS

                <br>Real-world entity 
                <br>Relation-based tables 
                <br>Isolation of data and application 
                <br>Less redundancy 
                <br>Consistency
